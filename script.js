//How to declare a variable
// var - variable
// let - let(scope)
// const - constant
let firstName = "John";
let lastName = "Doe";
let age = 21;

console.log(firstName);
console.log(lastName);
console.log(age);

//String Concatenation
console.log("My name is " + firstName + " " + lastName + ". I am " + age + " " + "years old");

//String Intepolation
console.log(`My name is ${firstName} ${lastName}. I am ${age} years old`);

